@app.post("/api/jobs")
async def create_job(
    url: str = Form(""),
    upload: UploadFile | None = File(None),
    source_type: str = Form("url"),
    save_video: bool = Form(False),
    save_audio: bool = Form(False),
    save_transcripts: bool = Form(False),
    generate_articles: bool = Form(False),
    article_styles: list[str] = Form([]),
    article_length: str = Form("long"),
    model: str = Form("small"),
    backend: str = Form("local"),
    mimo_api_key: str = Form(""),
    mimo_base_url: str = Form(""),
    mimo_model: str = Form("mimo-v2.5"),
    llm_preset: str = Form(""),
    llm_api_key: str = Form(""),
    llm_base_url: str = Form(""),
    llm_model: str = Form(""),
    language: str = Form("zh"),
    device: str = Form("cpu"),
    compute_type: str = Form("int8"),
    browser_download: bool = Form(True),
) -> dict[str, str]:
    if generate_articles:
        save_transcripts = True
    if not save_video and not save_audio and not save_transcripts:
        raise HTTPException(status_code=400, detail="Please select at least one output.")
    if backend not in {"local", "mimo"}:
        raise HTTPException(status_code=400, detail="Unsupported ASR backend.")

    job_id = uuid.uuid4().hex[:12]
    job = Job(id=job_id)
    jobs[job_id] = job

    input_file: Path | None = None
    source_url = extract_url(url) if url.strip() else None

    if source_type == "file":
        if not upload or not upload.filename:
            raise HTTPException(status_code=400, detail="Please choose a local video or audio file.")
        UPLOAD_DIR.mkdir(parents=True, exist_ok=True)
        input_file = UPLOAD_DIR / f"{job_id}_{Path(upload.filename).name}"
        with input_file.open("wb") as target:
            shutil.copyfileobj(upload.file, target)
        source_url = None
    elif not source_url:
        raise HTTPException(status_code=400, detail="Please paste a video URL.")

    llm_config = load_llm_model_config()
    llm_preset_data = get_llm_preset(llm_preset or llm_config["default"])
    if generate_articles and not llm_preset_data:
        raise HTTPException(status_code=400, detail="Selected writing model preset was not found.")

    preset_model = llm_preset_data["model"] if llm_preset_data else "mimo-v2.5"
    preset_base_url = llm_preset_data["base_url"] if llm_preset_data else ""
    preset_api_key_env = llm_preset_data["api_key_env"] if llm_preset_data else "LLM_API_KEY"

    kwargs = {
        "url": source_url,
        "input_file": input_file,
        "output_dir": OUTPUT_DIR,
        "model": model,
        "language": language or None,
        "device": device,
        "compute_type": compute_type,
        "backend": backend,
        "mimo_api_key": mimo_api_key.strip() or get_env_value("MIMO_API_KEY"),
        "mimo_base_url": mimo_base_url.strip() or get_env_value("MIMO_BASE_URL"),
        "mimo_model": mimo_model,
        "browser_download": browser_download,
        "browser_profile": BASE_DIR / "cookies" / "browser-profile",
        "browser_channel": "msedge",
        "save_video": save_video,
        "save_audio": save_audio,
        "save_transcripts": save_transcripts,
        "generate_article_outputs": generate_articles,
        "article_styles": article_styles or ["xiaohongshu", "professional"],
        "article_length": article_length if article_length in {"standard", "long", "deep"} else "long",
        "llm_api_key": (
            llm_api_key.strip()
            or get_env_value(preset_api_key_env)
            or get_env_value("LLM_API_KEY")
            or get_env_value("MIMO_API_KEY")
        ),
        "llm_base_url": llm_base_url.strip() or preset_base_url or get_env_value("LLM_BASE_URL") or get_env_value("MIMO_BASE_URL"),
        "llm_model": llm_model.strip() or preset_model or get_env_value("LLM_MODEL") or "mimo-v2.5",
    }

    asyncio.create_task(run_job(job, kwargs))
    return {"job_id": job_id}


@app.get("/api/jobs/{job_id}")
def get_job(job_id: str) -> dict[str, Any]:
    job = jobs.get(job_id)
    if not job:
        raise HTTPException(status_code=404, detail="Job not found.")
    return {
        "id": job.id,
        "status": job.status,
        "logs": job.logs,
        "result": job.result,
        "error": job.error,
        "created_at": job.created_at,
    }


@app.get("/api/files")
def list_files() -> dict[str, list[dict[str, str]]]:
    groups = {
        "videos": ("video", {".mp4", ".mkv", ".webm", ".mov"}),
        "audio": ("audio", {".m4a", ".mp3", ".wav", ".flac", ".ogg"}),
        "transcripts": ("transcripts", {".txt", ".srt", ".json"}),
        "articles": ("articles", {".md", ".txt", ".json"}),
    }
    response: dict[str, list[dict[str, str]]] = {}
    items_root = OUTPUT_DIR / "items"
    items_root.mkdir(parents=True, exist_ok=True)
    for key, (subdir, allowed_suffixes) in groups.items():
        files = sorted(
            (
                item
                for item in items_root.rglob("*")
                if item.is_file() and item.parent.name == subdir and item.suffix.lower() in allowed_suffixes
            ),
            key=lambda item: item.stat().st_mtime,
            reverse=True,
        )
        response[key] = [path_to_item(item) for item in files[:50] if path_to_item(item)]
    return response


@app.get("/files")
def download_file(path: str) -> FileResponse:
    requested = Path(path).resolve()
    allowed_roots = [OUTPUT_DIR.resolve()]
    if not any(requested == root or root in requested.parents for root in allowed_roots):
        raise HTTPException(status_code=403, detail="File is outside the outputs folder.")
    if not requested.exists() or not requested.is_file():
        raise HTTPException(status_code=404, detail="File not found.")
    return FileResponse(requested, filename=requested.name)
